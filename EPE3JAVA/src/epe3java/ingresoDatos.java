    /**
     * guarda el objeto en un archivo temporal tiene un try catch para captar si
     * hay errores de escritura del objeto
     *
     * @param Datos el arreglo de datos
     */
    public void guardar(ArrayList<Reservas> Datos) {
        try {
            ObjectOutputStream salida = new ObjectOutputStream(new FileOutputStream("datos.CUE"));
            salida.writeObject(Datos);
            salida.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + ex.getMessage(), "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

