    /**
     * metodo para agregar Items a un JComboBox primero elimina cualquier item
     * que tenga el elemento
     *
     * @param array un array tipo String[]
     * @param combo un elemento JComboBox
     */
    public void filljCombo(String[] array, javax.swing.JComboBox combo) {
        combo.removeAllItems();//elimina todos los Items
        for (int i = 0; i < array.length; i++) {
            combo.addItem(array[i]);//dentro del ciclo for va agregando un item del array al jcombobox
        }
    }

