    /**
     * lee el archivo temporal que guardamos en la interfaz de medico y lo
     * guarda en la variable global datos de tabla
     */
    public void ObtenerData() {
        try {
            ObjectInputStream entrada = new ObjectInputStream(new FileInputStream("datos.CUE"));
            DatosTabla = (ArrayList<Reservas>) entrada.readObject();
            entrada.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + ex.getMessage(), "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * llena el modelo para insertar en la tabla, recorre un ciclo for para
     * agregar las filas de datos
     */
    public void llenarTabla() {
        tabla.addColumn("Nombre MÃ©dico");
        tabla.addColumn("Nombre Paciente");
        tabla.addColumn("Especialidad");
        tabla.addColumn("Horas Disponibles");
        tablaReservas.setModel(tabla);
        for (int i = 0; i < DatosTabla.size(); i++) {
            Object[] fila = {
                DatosTabla.get(i).getMedico(), DatosTabla.get(i).getPaciente(),
                DatosTabla.get(i).getEspecialidad(), DatosTabla.get(i).getDisponibilidad()
            };
            tabla.addRow(fila);
        }
    }

